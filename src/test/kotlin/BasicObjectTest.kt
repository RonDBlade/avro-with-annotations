import com.example.Person
import com.example.testsuite.BasicObject
import org.jetbrains.annotations.NotNull
import org.junit.jupiter.api.Assertions
import org.junit.jupiter.api.DisplayName
import org.junit.jupiter.api.Test
import java.time.Instant

class BasicObjectTest {

    @DisplayName("Test conversion of stuff using explicit auto-typing.")
    @Test
    fun `test conversion of stuff`() {
        val objecte = BasicObject.newBuilder()
            .setPrimitive(1)
            .setNullablePrimitive(10 as Int?)
            .setString("value" as String?)
            .setNullableString("value2" as String?)
            .setAutoGeneratedJavaClass(Instant.ofEpochMilli(100) as Instant?)
            .setNullableAutoGeneratedJavaClass(Instant.ofEpochMilli(1000) as Instant?)
            .setUnionType(500 as Int?)
            .setNullableUnionType("500" as String?)
            .build();

        val primitive: Int = objecte.primitive
        val nullablePrimitive: Int? = objecte.nullablePrimitive
        val string: String = objecte.string
        val nullableString: String? = objecte.nullableString
        val autoGeneratedJavaClass: Instant = objecte.autoGeneratedJavaClass
        val nullableAutoGeneratedJavaClass: Instant = objecte.autoGeneratedJavaClass
        val unionType: Any = objecte.unionType
        val nullableUnionType: Any? = objecte.nullableUnionType
    }
}